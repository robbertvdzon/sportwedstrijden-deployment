create table users (
    id bigint generated by default as identity,
    passwd varchar(255),
    username varchar(255),
    permissions varchar(255),
    uuid  varchar(255),
    primary key (id)
);

create table teammember (
    id bigint generated by default as identity,
    uuid  varchar(255),
    nickname  varchar(255),
    user_id bigint,
    team_id bigint,
    primary key (id)
);

create table team (
    id bigint generated by default as identity,
    uuid  varchar(255),
    teamname  varchar(255),
    primary key (id)
);

create table competition (
    id bigint generated by default as identity,
    uuid  varchar(255),
    team_id bigint,
    competitionname  varchar(255),
    primary key (id)
);

create table game (
    id bigint generated by default as identity,
    uuid  varchar(255),
    tegenpartij  varchar(255),
    competition_id bigint,
    primary key (id)
);

